				<p class="lead">Shaastra 2014's motto was "Breaking Boundaries", and to this regard, a number of initiatives were taken up and executed. Among them were movements like Computer Literacy For All, Intellectual Property Rights Awareness Drive, Ideas Challenge and Shaastra Sparsh.  <b>Computer Literacy For All</b>, expanded from its successful 2013 edition, was a movement that aimed at productively dealing with e-waste. The computers collected by the institute were serviced and subsequently distributed in a school in the college's rural neighborhood. <b>The Intellectual Property Rights Awareness Drive</b> was an initiative aimed at spreading awareness about IPR and copyright protection among the youth through fun initiatives such as quizzes and competitions. <br><b>Ideas Challenge</b> was a competition where participants attempted to solve socially-relevant problem statements suggested by NGOs and RuTAG using their technical skills and engineering knowledge. <b>Shaastra Sparsh</b> was a movement that associated with schools in Chennai, aimed at interacting with younger children.  In addition to such wholly independent initiatives, a number of events at Shaastra are themed with relevant social issues in the backdrop. Events which have sustainable development and environment-friendly issues as their central theme are a rapidly evolving genre at Shaastra. For example, Shaastra 2014 had <b>Sustainable Cityscape</b> as a key event. <b>Shaastra Symposium</b>, a key event in any iteration of Shaastra, serves as a platform for rigourous debate on relevant social issues. For example, Shaastra 2013 Symposium discussed "Education and Governance", Shaastra 2014's symposium was on "Agriculture in India". </p>
